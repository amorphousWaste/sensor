"""Unicode values."""


class UnicodeConstants(object):
    """Constants."""

    def __init__(self):
        """Init."""
        super(UnicodeConstants, self).__init__()

        # Unicode symbol strings
        self.celsius = '\u00B0C'
        self.fahrenheit = '\u00B0F'
        self.ohm = '\u03A9'
        self.sds011_conc = '\u00B5g/m\u00B3'  # Micrograms per cubic meter
        self.subscript_2 = '\u2082'
